# Ejercicio de evaluación 1 de Programación II
# Se requiere almacenar en dos listas los nombres y las edades de un grupo de estudiantes.
# 
# Realizar una función que ayude a cargar los datos de los estudiantes.
# 
# Realizar otra función que calcule el promedio de edad de los estudiantes.
# 
# Realizar una tercera función que muestre que diga cuál es el estudiante mas joven.
#
# Realizar una cuarta función que muestre cuál es el estudiante más viejo.
# 
# Realizar una quinta función que diga cuantos estudiantes son mayores de edad.
#
# Al finalizar, el programa deberá mostrar todos los datos calculados de las funciones anteriores.